package tiposprimitivosflotantes;

/**
 *
 * @author Federico
 */
public class TiposPrimitivosFlotantes {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Tipos Primitivos de tipo Float : float y double

        float numeroFloat = (float) 10.0;
        System.out.println("numeroFloat = " + numeroFloat);
        System.out.println("Maximo valor de Float : " + Float.MAX_VALUE);
        System.out.println("Minimo valor de Float : " + Float.MIN_VALUE);

        double numeroDouble = 10;
        System.out.println("numeroDouble = " + numeroDouble);
        System.out.println("Maximo valor de Double : " + Double.MAX_VALUE);
        System.out.println("Minimo valor de Dobule : " + Double.MIN_VALUE);

    }

}
