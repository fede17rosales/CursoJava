package tiposprimitivoschar;

/**
 *
 * @author Federico
 */
public class TiposPrimitivosChar {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        char miCaracter = 'a';
        System.out.println("miCaracter = " + miCaracter);

        char varChar = '\u0021';
        System.out.println("varChar = " + varChar);

        char varCharDecimal = 33;
        System.out.println("varCharDecimal = " + varCharDecimal);

        char varCharSimbolo = '!';
        System.out.println("varCharSimbolo = " + varCharSimbolo);

        // var
        // var miCaracter = 'a';
        // System.out.println("miCaracter = " + miCaracter);
        var varChar2 = '\u0021';
        System.out.println("varChar = " + varChar2);

        var varCharDecimal2 = 33;
        System.out.println("varCharDecimal = " + varCharDecimal2);

        var varCharSimbolo2 = '!';
        System.out.println("varCharSimbolo = " + varCharSimbolo2);

    }

}
